#!/usr/bin/env bash
# ai-os-heartbeat.sh — Claude Code hook. Writes a machine heartbeat + git
# snapshot into the project's .ai/ so the watchdog (and the next agent) can
# tell whether an agent is alive and what it was doing.
#
# Wired in ~/.claude/settings.json on: SessionStart, PostToolUse, Stop, SessionEnd.
# Receives hook JSON on stdin. MUST never block or fail Claude — always exit 0.
#
# Throttle: PostToolUse only writes if >THROTTLE since last beat. Lifecycle
# events (SessionStart/Stop/SessionEnd) always write.

THROTTLE=900   # 15 min, matches the gap we accept while an agent is alive

main() {
  command -v jq >/dev/null 2>&1 || exit 0
  local input; input="$(cat 2>/dev/null || true)"
  [ -n "$input" ] || exit 0

  local event cwd sid tpath tool reason source
  event=$(jq -r '.hook_event_name // empty' <<<"$input" 2>/dev/null)
  cwd=$(jq -r '.cwd // empty'             <<<"$input" 2>/dev/null)
  sid=$(jq -r '.session_id // empty'      <<<"$input" 2>/dev/null)
  tpath=$(jq -r '.transcript_path // empty' <<<"$input" 2>/dev/null)
  tool=$(jq -r '.tool_name // empty'      <<<"$input" 2>/dev/null)
  reason=$(jq -r '.reason // empty'       <<<"$input" 2>/dev/null)
  source=$(jq -r '.source // empty'       <<<"$input" 2>/dev/null)

  [ -n "$cwd" ] && cd "$cwd" 2>/dev/null || exit 0
  # only act inside a git repo that has been bootstrapped (.ai/ present)
  git rev-parse --git-dir >/dev/null 2>&1 || exit 0
  [ -d .ai ] || exit 0

  local hb=".ai/heartbeat.json" now last
  now=$(date +%s)

  # throttle PostToolUse
  if [ "$event" = "PostToolUse" ] && [ -f "$hb" ]; then
    last=$(jq -r '.last_beat_epoch // 0' "$hb" 2>/dev/null || echo 0)
    if [ "$last" -gt 0 ] && [ $((now - last)) -lt "$THROTTLE" ]; then
      exit 0
    fi
  fi

  local status branch dirty nfiles iso
  case "$event" in
    SessionEnd) status="ended" ;;
    Stop)       status="idle"  ;;   # turn finished; may resume on next prompt
    *)          status="active" ;;
  esac
  branch=$(git rev-parse --abbrev-ref HEAD 2>/dev/null || echo "?")
  nfiles=$(git status --porcelain 2>/dev/null | wc -l | tr -d ' ')
  [ "${nfiles:-0}" -gt 0 ] && dirty=true || dirty=false
  iso=$(date -u +%Y-%m-%dT%H:%M:%SZ)

  jq -n --arg agent claude --arg sid "$sid" --arg ev "$event" \
        --arg cwd "$cwd" --arg br "$branch" --arg tp "$tpath" \
        --arg tool "$tool" --arg st "$status" --arg iso "$iso" \
        --arg reason "$reason" --arg source "$source" \
        --argjson dirty "$dirty" --argjson nf "${nfiles:-0}" \
        --argjson now "$now" '{
    agent:$agent, tool:"claude-code", session_id:$sid, event:$ev,
    source:$source, end_reason:$reason, cwd:$cwd, branch:$br,
    transcript_path:$tp, last_tool:$tool, status:$st,
    dirty:$dirty, uncommitted_files:$nf,
    last_beat:$iso, last_beat_epoch:$now
  }' > "$hb" 2>/dev/null || true

  # mechanical git snapshot the agent's narrative HANDOFF.md can't capture
  {
    echo "# .ai/HANDOFF.auto.md — machine snapshot (regenerated by ai-os-heartbeat)"
    echo "# NOT the narrative handoff. The agent owns HANDOFF.md; this is git state."
    echo
    echo "Updated: $iso  |  session: $sid  |  event: $event  |  status: $status"
    echo "Branch: $branch  |  uncommitted files: ${nfiles:-0}"
    echo
    echo "## git status --short"
    git status --short 2>/dev/null | head -50
    echo
    echo "## last 5 commits"
    git log --oneline -5 2>/dev/null
  } > .ai/HANDOFF.auto.md 2>/dev/null || true

  exit 0
}
main "$@" 2>/dev/null
exit 0
